package Portfolio2;

public class 繰り返しfor {

	public static void main(String[] args) {

//		①5回繰り返す
		for(int i = 0; i < 5; i++) {
			System.out.println("処理");
		}
//		②開始の変数が0で、10回繰り返す
		for(int i = 0; i < 10; i++) {
			System.out.println("9");
		}
//		③開始の変数が1で、10回繰り返す
		for(int i = 1; i < 10; i++) {
			System.out.println("LO");
		}
//		④②の問題を自分で決めた好きな回数分繰り返す(右の数字未満で繰り返す)
		for(int i = 3; i < 19; i++) {
			System.out.println("nem");
		}
	}

}
