package Portfolio1;

public class 条件分岐 {

	public static void main(String[] args) {
		
//		int型の変数xが10以上の場合
		int x = 10;
		if(x >= 10) {
			System.out.println("Hellox");
		}		
//		double型の変数yが100未満の場合
		double y = 99;
		if(y < 100) {
			System.out.println("Helloy");
		}
//		int型の変数zの値がint型の変数xと同じ場合
		int z = 10;
		if(z == x) {
			System.out.println("Helloz");
		}
		

	}

}
